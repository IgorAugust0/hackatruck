/* Comentários são utilizados por programadores para facilitar o entendimento de seu código. 
As palavras contidas nos comentários são descartadas pelo compilador e servem somente para o desenvolvedor 
ou time de desenvolvedores produzirem um código que seja de mais fácil manutenção e legibilidade. 
Os comentários em Swift lembram os de C e Java. Existem em dois formatos principais: por linha ou em blocos. 
Vamos ver cada um deles.

Se desejamos comentar uma única linha, podemos utilizar o “//”, nesse caso o compilador desconsidera todo o trecho que vai do “//” até o final da linha, como no exemplo: */

print("Hello, World!") // irá imprimir uma saudação
//print("Hello, World Comentado!")

/* Existe também a possibilidade de um comentário em bloco que se inicia com a expressão “/*” e finaliza com a expressão “*/”, 
podendo até ser escrito por múltiplas linhas. Veja um exemplo: */

