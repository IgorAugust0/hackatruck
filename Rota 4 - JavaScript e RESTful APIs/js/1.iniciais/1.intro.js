// O JavaScript começou a ser desenvolvido por Brendan Eich em 1995. 
// O motivo foi a necessidade de se criar uma linguagem para o browser Netscape, empresa onde ele trabalhava. 
// Inicialmente, a linguagem se chamava Mocha e posteriormente mudaram seu nome para LiveScript e então, após um acordo com a Sun Microsystems, para JavaScript.

// Em setembro de 1995, o LiveScript foi lançado na versão beta do navegador Netscape 2.0 e teve seu nome alterado para JavaScript em dezembro do mesmo ano, na versão 2.0B3 do navegador.

// Pelo nome, JavaScript, podemos pensar que a linguagem compartilha as características da linguagem Java, porém esse é um engano comum. 
// A palavra 'java' foi introduzida no nome da linguagem, apenas como uma estratégia de marketing da Netscape para aproveitar da popularidade da linguagem Java quando o JavaScript foi lançado. 
// As duas linguagens possuem sintaxe, semânticas e usos muito diferentes.

// A aceitação do JavaScript como linguagem client-side das páginas web foi grande, que significa que a linguagem roda diretamente no navegador web do computador do usuário, e como consequência a Microsoft criou um dialeto compatível com o JavaScript chamado de JScript e o incluiu no Internet Explorer 3.0 em agosto de 1996. 
// Apesar de terem nomes diferentes, JavaScript e JScript são muito similares.

// Em 1996, a Netscape submeteu o JavaScript como candidato a padrão industrial para a European Computer Manufacturers Association, o que resultou na versão padronizada chamada ECMAScript. 
// A ECMA é uma organização criada com o objetivo de ser responsável pela padronização de sistemas de informação.

// Desde 2012, todos os navegadores modernos possuem suporte total ao ECMAScript 5.1. 
// A sexta versão do ECMAScript foi publicada em 2017 e chamada de ECMAScript 2015. 
// Ela também foi conhecida inicialmente como ECMAScript 6 ou ES6