// 4. Quais dos itens abaixo são importantes para uma API ser considerada RESTful ela precisa?

// Client-server, Stateless, Cacheable.

// Client-side, Tableless, Cacheable.

// Client-code, Stateless, Cacheable.

// Client-server, Tableless, Cacheable.

// Resposta: Client-server, Stateless, Cacheable.
// Client-server = O cliente e o servidor devem ser independentes um do outro. O cliente não precisa saber como o servidor funciona e vice-versa.
// Stateless = Cada requisição que o cliente faz para o servidor deve conter todas as informações necessárias para o servidor entender e responder (Response) a requisição (Request). O servidor não deve guardar estado das requisições anteriores. Exemplo: A sessão do usuário não deve ser mantida no servidor.
// Cacheable = As respostas para requisições devem ser explicitas ao dizer se aquela requisição pode ou não ser cacheada pelo cliente.

